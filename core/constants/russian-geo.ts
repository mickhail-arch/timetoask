export function searchCities(query: string, limit = 5): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return RUSSIAN_CITIES.slice(0, limit);

  const startsWith: string[] = [];
  const includes: string[] = [];

  for (const city of RUSSIAN_CITIES) {
    const low = city.toLowerCase();
    if (low.startsWith(q)) startsWith.push(city);
    else if (low.includes(q)) includes.push(city);
  }

  return [...startsWith, ...includes].slice(0, limit);
}

