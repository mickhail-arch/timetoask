const FORBIDDEN_WORDS: string[] = [
  'блять', 'бля', 'блядь', 'блядина', 'блядство',
  'ебать', 'ебаный', 'ебанутый', 'ебло', 'ебальник', 'заебал', 'заебись', 'уебок', 'уебище', 'выебон',
  'хуй', 'хуйня', 'хуёвый', 'хуёво', 'охуеть', 'охуенный', 'нахуй', 'похуй',
  'пизда', 'пиздец', 'пиздёж', 'пиздатый', 'пиздить', 'спиздить', 'пиздабол',
  'сука', 'сучка', 'сучара',
  'мудак', 'мудила', 'мудозвон',
  'дерьмо', 'говно', 'говнюк', 'засранец',
  'жопа', 'жопошник',
  'шлюха', 'проститутка', 'потаскуха',
  'педик', 'пидор', 'пидорас',
  'взрывчатка', 'террорист', 'терроризм', 'джихад', 'игил',
  'убить', 'убийство', 'расстрел', 'геноцид',
  'героин', 'кокаин', 'метамфетамин', 'амфетамин',
  'марихуана', 'гашиш', 'экстази', 'лсд',
  'наркотик', 'наркоман', 'наркота', 'закладка', 'барыга',
];

export interface FrontFilterResult {
  clean: boolean;
  found: string[];
}

export function frontFilterClient(text: string): FrontFilterResult {
  const lower = text.toLowerCase();
  const found: string[] = [];
  for (const word of FORBIDDEN_WORDS) {
    if (lower.includes(word.toLowerCase())) {
      found.push(word);
    }
  }
  return { clean: found.length === 0, found };
}
